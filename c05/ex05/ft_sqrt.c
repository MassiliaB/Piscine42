#include <unistd.h>

int	ft_sqrt(int nb)
{
	int i;

	if (nb < 1)
		return (0);
	if (nb == 1)
		return (1);
	i = 1;
	while (i <= nb / i + 1)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
